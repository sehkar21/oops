package Javabasics;

public class Stringconcatenation {

	public static void main(String[] args) {
		int a= 10;
		int b=20;
		 String s= "hello";
		 String f="world";
		  
		 System.out.println(a+b);
		System.out.println(s+f);
		System.out.println(a+b+s+f);
		System.out.println(s+f+a+b);
		System.out.println(s+f+(a+b));
		
		System.out.println(a+b+s+f+a+b+s);
		
	}
	
	
	
	
}
